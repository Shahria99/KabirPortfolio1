//Dates
let myDate = new Date();
console.log(myDate);

console.log(myDate.toString());
//Thu Apr 04 2024 14:43:06 GMT+0600 (Bangladesh Standard Time)

console.log(myDate.toDateString());
//Thu Apr 04 2024

console.log(myDate.toISOString());
//2024-04-04T08:46:14.919Z

console.log(myDate.toJSON());
//2024-04-04T08:46:49.720Z

console.log(myDate.toLocaleDateString());
// 4/4/2024

console.log(myDate.toLocaleString());
// 4/4/2024, 2:48:06 PM

